CREATE TABLE IF NOT EXISTS `APP_AGENDA` (
  `AGD_ID` BIGINT(4) NOT NULL AUTO_INCREMENT,
  `AGD_DATA` DATE NOT NULL,
  `AGD_HORA` TIME NOT NULL,
  `AGD_FONECE` VARCHAR(6) NOT NULL,
  `AGD_LOJA` VARCHAR(2) NOT NULL,
  `AGD_PEDIDO_COMPRA` VARCHAR(6) NOT NULL,
  `AGD_NF` VARCHAR(9) NOT NULL,
  `AGD_CHAVE` VARCHAR(44) NOT NULL,
  `AGD_TIPO_CAMINHAO` BIGINT(4) NOT NULL,
  `AGD_TIPO_PRODUTO` BIGINT(4) NOT NULL,
  `AGD_VOLUME` DECIMAL(8) NOT NULL,
  `AGD_TRASNP` VARCHAR(6) NOT NULL,
  `AGD_LOJA_TRASNP` VARCHAR(2) NOT NULL,
  `AGD_VALOR_DESCARGA` DECIMAL(8) NOT NULL,
  `AGD_NF_DEVOLUCAO` VARCHAR(9) NOT NULL,
  `AGD_CHAVE_DEVOLUCAO` VARCHAR(44) NOT NULL,
  `AGD_SENHA_DESCARGA` VARCHAR(10) NOT NULL,
  `AGD_STATUS` varchar(1) NOT NULL,
  `D_E_L_E_T_` varchar(1) NOT NULL,
  PRIMARY KEY (`AGD_ID`)
)
